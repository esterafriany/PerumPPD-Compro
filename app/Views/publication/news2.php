				<h2>PERUM PPD LAKUKAN PENANDATANGANAN MOU BUS LISTRIK DENGAN PT SAG</h2>
				<p>Rabu, 10 November 2021</p>
				<img src="<?php echo base_url();?>/assets/images/news/53407.jpg" class="img-fluid" width="800px" height="700px"><br/><br/>
				Perusahaan Umum Pengangkutan Penumpang Djakarta (Perum PPD) menandatangani Nota Kesepahaman (Memorandum of Understanding) kerjasama penyediaan bus listrik Perum PPD dengan PT. Sinar Armada Globalindo di Jakarta, Rabu (10/11/2021).
				MoU ditandatangani oleh Direktur Utama Perum PPD Pande Putu Yasa dengan Direktur Utama PT. Sinar Armada Globalindo Johannes Herwanto. </br></br>
				Perum PPD akan melakukan konversi peremajaan bus berbahan bakar diesel menuju bus listrik sebagai upaya pembuktian konsistensi Perum PPD dalam rangka pengurangan polusi udara yang disebabkan oleh penggunaan pribadi dan mendukung program pemerintah menjadikan kendaraan di Indonesia ramah lingkungan dengan menggunakan kendaraan listrik termasuk bus. 

				Dalam rencana peremajaan bus tersebut, Perum PPD berencana melakukan pengadaan 100 unit bus listrik di 2022 yang bekerjasama dengan produsen dan APM seperti Golden Dragon.</br></br>
				Pelaksanaan vaksinasi berjalan dengan lancar dibawah kepemimpinan Lurah Bapak Faisal. Terlihat antrean yang tertata, 
				alur penyuntikan yang sangat memperhatikan protokol kesehatan hingga pos akhir pencatatan PeduliLindungi yang presisi.</br></br>
				
				Berbeda dengan kerjasama bus listrik sebelumnya, kali ini Perum PPD menggandeng PT. Sinar Armada Globalindo dengan produk bus listrik bernama SAG.

				SAG merupakan bus listrik yang teringan dikelasnya dengan berat asli dalam keadaan kosong hanya 12.332 Kg serta dilengkapi oleh fire protection system dan hepa filter membuat bus dengan kapasitas 60 penumpang ini mantap digunakan untuk peruntukan sebagai angkutan umum perkotaan.

				Pande Putu Yasa menyambut dengan hangat kerjasama kedua perusahaan dalam produksi dan pengoperasian bus listrik ini. Sehingga Perum PPD dapat memulai sejarah baru pengoperasian bus listrik untuk angkutan umum di Ibu Kota Jakarta.</br></br>
				
				Diharapkan penandatangan MoU ini dimulainya bagian sejarah baru transpotasi umum di Ibu Kota dengan mulai beralih dari penggunaan transportasi pribadi sehingga menciptakan ekosistem dari kendaraan listrik di Indonesia bisa semakin cepat berkembang dibandingkan dengan sebelum-sebelumnya. 	</br></br>
				
				Menjelang akhir tahun 2021, sebagai pelopor operator bus listrik terbesar Perum PPD telah melakukan penandatangan Nota Kesepahaman (Memorandum of Understanding) dengan lebih dari 6 Agen Pemegang Merek (APM) serta melayani uji coba bus litrik sejak tahun 2019 dan mengoperasikannya di berbagai kota di Indonesia.</br></br>
