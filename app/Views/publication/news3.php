				<h2>DISKON JR CONNEXION PERUM PPD PERUM PPD BAGI MASYARAKAT KOTA BOGOR</h2>
				<p>Jumat, 2 Juli 2021</p>
				<img src="<?php echo base_url();?>/assets/images/news/604711304.jpeg" class="img-fluid" width="800px" height="800px"><br/><br/>
				Perusahaan Umum Pengangkutan Penumpang Djakarta (Perum PPD) memberikan diskon kepada pelanggan Jabodetabek Residence Connexion (JR Connexion) Perum PPD secara cuma-cuma selama sebulan penuh dalam rangka memperingati Hari Jadi Kota Bogor ke 539 Tahun.<br><br> 

				Sebagai operator bus yang beroperasi di wilayah Jabodetabek, Perum PPD mengapresiasi animo masyarakat yang tinggi serta dukungan Pemerintah Kota Bogor terhadap kehadiran JR Connexion Perum PPD di Kota Bogor. Oleh karena itu, Perum PPD memberikan diskon kepada para pengguna layanan untuk kalangan TNI, Polri, Tenaga Kesehatan, Pelajar, dan Mahasiswa yang telah menjadi pelanggan JR Connexion Perum PPD dengan asal rute Kota Bogor yakni Grand Central Bogor dan Taman Sari Persada. Program pemberian potongan tarif ini akan berlangsung selama 1 (satu) bulan terhitung sejak 16 Juni sampai dengan 16 Juli 2021.</br></br>
				
				Pemberian potongan tarif ini bertujuan sebagai ucapan terima kasih dan apresiasi kepada para pengabdi negara dan garda terdepan yang tidak kenal lelah bekerja ditengah pandemi serta menyambut pelajar/mahasiswa yang akan kembali melakukan kegiatan pembelajaran offline atau tatap muka.<br><br>
				
				Pemberian diskon ini secara simbolis dilakukan oleh Wakil Walikota Bogor Bapak Dedie A. Rachim dan Direktur Utama Perum PPD Bapak Pande Putu Yasa kepada 5 perwakilan pelanggan dari masing-masing instansi dengan memberikan Kartu Free Pass JR Connexion Perum PPD. Serta dihadiri oleh Plt. Direktur Angkutan Badan Pengelola Transportasi Jabodetabek (BPTJ) Bapak Saptandri, Kepala Dinas Pariwisata dan Kebudayaan Kota Bogor Bapak Atep Budiman, Kabid Angkutan Jalan Dinas Perhubungan Kota Bogor Bapak Mulyadi, dan petinggi lainnya dari Pemerintah Kota Bogor serta Kementerian Perhubungan.<br><br>
				
				Pande Putu Yasa selaku Direktur Utama Perum PPD menyatakan bahwa program ini sebagai wujud kontribusi Perum PPD untuk Kota Bogor, “Perum PPD memberikan kontribusi untuk masyarakat Kota Bogor dalam hari jadinya yang ke-539. Manfaat kontribusi ini dapat dirasakan oleh seluruh jajaran TNI, Polri, Tenaga Kesehatan, Mahasiswa dan Pelajar di Kota Bogor.”<br></br>
				
				Kontribusi dari Perum PPD ini sejalan dengan pernyataan dari Bapak Dedhie A. Rachim selaku Wakil Walikota Bogor yang mendukung keberadaan Perum PPD sebagai operator bus Jabodetabek, “Siap mendukung terus keberadaan dan semua program yang dilakukan oleh Perum PPD di Kota Bogor. Tidak perlu ada keraguan dalam memasuki daerah-daerah di Jabodetabek.”

				Pelanggan akan diberikan Kartu Free Pass untuk dapat digunakan ketika menaiki bus JR Connexion selama dalam tenggang waktu yang telah ditentukan. Untuk Informasi lebih lanjut pelanggan dapat menghubungi sosial media dan Customer Service Perum PPD.<br><br>
				
