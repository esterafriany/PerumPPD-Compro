				<h2>PERUM PPD dan Kelurahan Kebon Pala Bersinergi Laksanakan Vaksinasi di Kebon Pala</h2>
				<p>Senin, 4 April 2022</p>
				<img src="<?php echo base_url();?>/assets/images/news/vaksin2.jpg" class="img-fluid" width="1000px" height="1000px"><br/><br/>
				Perusahaan Umum Pengangkutan Penumpang Djakarta (Perum PPD) menjadi salah satu perusahaan yang mendorong 
				dan memfasilitasi vaksinasi di RPTRA Kebon Pala, Jakarta Timur. Kegiatan ini dilakukan untuk mewujudkan 
				Indonesia bebas Covid-19 serta bukti bahwa Perum PPD juga turut mendukung program yang dibuat oleh 
				kelurahan Kebon Pala. </br></br>
				Pada saat ini Perum PPD bersinergi dengan kelurahan Kebon Pala, Jakarta Timur untuk pelaksanaan kegiatan 
				vaksinasi masyarakat umum dan anak usia 6-11 tahun. Kegiatan ini juga merupakan rangkaian program kesehatan 
				yang dimiliki oleh kelurahan Kebon Pala dalam upaya memutuskan rantai penyebaran Covid-19 di pemukiman padat 
				penduduk.</br></br>
				Pelaksanaan vaksinasi berjalan dengan lancar dibawah kepemimpinan Lurah Bapak Faisal. Terlihat antrean yang tertata, 
				alur penyuntikan yang sangat memperhatikan protokol kesehatan hingga pos akhir pencatatan PeduliLindungi yang presisi.</br></br>
				Kegiatan vaksinasi di kelurahan Kebon pala berlangsung dengan baik, berkat kerja sama dan kolaborasi yang baik. Dengan 
				melaksanakan kegiatan ini diharapkan terus ada kerjasama yang terus ditingkatkan dalam berbagai program yang lainnya. 
				Terutama kegiatan dalam rangka mencegah penularan Covid-19 di lingkungan Kebon Pala.</br></br>
				
				Sebelumnya juga Perum PPD telah melaksanakan kegiatan vaksinasi bersama Sentra Vaksinasi Bersama (SVB) BUMN yang diadakan 
				di Tennis Indoor Senayan, sentra vaksin ini merupakan inisiasi dari Kementrian BUMN dan Kementrian kesehatan untuk 
				mempercepat vaksinasi untuk masyarakat. Vaksinasi ini ada di lima kota yaitu, Jakarta, Bandung, Semarang, Purwokerto, 
				dan Surabaya.</br></br>
				
				Dalam dukungan penuh Perum PPD juga memberikan rasa aman untuk karyawan dan penumpang dengan melalukan vaksinaksi kepada 
				seluruh karyawan tanpa terkecuali. Lewat dukungan dan komitmen untuk karyawan ini diharapkan bisa memutus rantai 
				Covid-19, menjadikan penumpang serta  sesama karyawan merasa aman terhadap lingkungan sekitar.</br></br>
				
				Demi mencegah timbulnya klaster Covid-19 di lingkungan Perum PPD, Perum PPD sangat menjaga kebersihan dan juga menerapkan 
				protokol 5M (Mencuci tangan, Menjaga jarak, Memakai masker, Menjauhi kerumunan, Membatasi Mobilisasi). Bukti dari 
				menjaga kebersihan ini juga karena Perum PPD membangun sektor cuci tangan selain karena kebersihan tetapi juga 
				mengkatkan kesadaran karyawan akan Covid-19. </br></br>
