			Berdasarkan Peraturan Pemerintah No. 205 tahun 1961, Perusahaan berubah status menjadi Perusahaan Negara dibawah naungan Departemen Perhubungan Darat, Pos, Telekomunikasi, dan Pariwisata. Kemudian berdasarkan Peraturan Pemerintah No. 229 tahun 1961 tanggal 20 September 1961, pengelolaan PN PPD diserahterimakan dari Departemen Perhubungan Darat, Pos, Telekomunikasi, dan Pariwisata kepada Pemerintah Daerah DKI Jakarta. Pada tahun 1981, pengelolaan PN PPD kembali ke Pemerintah Pusat Departemen Perhubungan berdasarkan Peraturan Pemerintah No. 23 tahun 1981 tanggal 17 Juli 1981. Selanjutanya berdasarkan Peraturan Pemerintah No. 24 tahun 1981 terjadi perubahan status Perusahaan Negara Pengangkutan Penumpang Djakarta (PN PPD) menjadi Perusahaan Umum Pengangkutan Penumpang Djakarta (Perum PPD).<br></br>
						
						Bentuk badan hukum Perum PPD dituangkan dalam Peraturan Pemerintah No. 32 tahun 1984 tentang Perum PPD sebagai penyempurnaan untuk menjiwai Tata Cara Pembinaan dan Pengawasan sebagaimana diatur dan dituangkan dalam Peraturan Pemerintah No. 3 tahun 1983 tentang Tata Cara pembinaan Perjan, Perum, dan Pesero. <br></br>
						Dengan diundangkannya Peraturan Pemerintah No. 13 tahun 1998 tentang Perusahaan Umum, maka Peraturan Pemerintah No. 32 tahun 1984 tentang Perusahaan Umum Pengangkutan Penumpang Djakarta telah dicabut dan dinyatakan tidak berlaku lagi. Untuk itu telah diterbitkan Peraturan Pemerintah No. 91 tahun 2000 tentang Perusahaan Umum Pengangkutan Penumpang Djakarta. <br></br>
						
						Pada tahun 2013 Perum PPD melakukan transformasi bisnis dari layanan bus kota menjadi layanan Bus Rapid Transportasi. Pada tahun ini pula Perum PPD bertransformasi dan menyehatkan manajemen di seluruh aspek strategis antara lain financial, SDM, dan operasional. Strategi pembenahan yang diterapkan adalah fokus pada membentuk manajemen perusahaan yang sehat dan transparan sehingga Perum PPD pertamakali  dapat meraup laba.<br></br>
						
						Perum PPD turut mengembangkan IT guna memposisikan diri sebagai perusahaan transportasi umumyang handal dan terprcaya. Selain restrukturisasi SDM hingga mengubah budaya perusahan, perusahaan juga tumbuh sesuai dengan perkembangan zamannya guna memberikan pelayanan terbaik kepada masyakarat. Keterkaitan dengan para pemakai jasa transportasi ini pun tak lepas dari faktor produk, layanan, dan keterjangkauan. Tercatat kini seluruh bus yang berjumlah sekitar 700 unit ini, di dukung dengan Aplikasi Fleet Management System ( FMS ). FMS memiliki berbagai fungsi seperti pembiayaan kendaraan, perawatan kendaraan, telematika kendaraan ( pelacakan dan diagnostic ), driver management, manajem bahan bakar dan manajemen kesehatan dan keselamatan. Fleet management System adalah fungsi yang memungkinkan perusahaan-perusahaan yang mengandalkan transportasi  dalam bisnis mereka untuk menghapus atau meminimalkan risiko yang terkait dengan investasi kendaran, meningkatkan efisiensi, produktivitas dan mengurangi biaya keseluruhan transportasi perusahaan, dan menyediakan 100% sesuai dengan peraturan pemerintah ( tugas perawatan ) serta untuk kenyamanan penumpang. <br></br>
						
						Seiring geliat bisnis dan kinerjanya, perusahaan juga terus menjalin kemitraan dengan pihak lainnya. Di samping bus bantuan Kementerian Perhubungan yang sudah melayani 21 rute busway. Perum PPD juga memiliki beberapa SBU yang terdiri dari SBU Transbusway, SBU Pemeliharaan dan Perbaikan bus, SBU Transjabodetabek, SBU Aset dan Logistik, SBU Trans PPD MAC.<br></br>
						Sebagai raja jalanan Perum PPD terus mengembangkan bisnisnya yaitu Jabodetabek Airport Connexion yang melayani trayek Bandara Soekarno Hatta – Mall Taman Anggrek, Bandara Soekarno Hatta – ITC Cempaka Mas dan sebaliknya.
