			Sejarah singkat berdirinya SBU :
			Bahwa sejalan dengan pengembangan usaha perusahaan dan untuk sebagai operator pengoperasian Bus Transjabodetabek serta dalam rangka mendukung dan memenuhi kebutuhan interaksi sosial dan kegiatan sehari hari perlu didukung sistem angkutan umum massal perkotaan yang efektif, efisien, berbasis teknologi informasi dan terintegrasi dikawasan bandara dan perkotaan serta permukiman di wilayah Jabodetabek.
			<br/><br/>
		
			Ruang Lingkup SBU (Pokok Pekerjaan):<br/>
			SBU Transjabodetabek mempunyai tugas pokok melakukan:<br/>
			a. Pengelolaan alat produksi dan kekayaan perusahaan di lingkungan SBU Transjabodetabek.
			<br/>b. Pengusahaan pengoperasian, dan pekerjaan mutu layanan atas pemeliharaan bus yang dilaksanakan oleh SBU Pemeliharaan dan Perbaikan Bus.
			<br/>c. Pengelolaan sumber daya manusia sesuai dengan kebijakan yang telah ditetapkan perusahaan,
			<br/>d. Pengusahaan jasa pelayanan angkutan orang berbasis jalan sesuai dengan standar pelayanan minimum yang telah ditetapkan kepada masyarakat serta memupuk keuntungan berdasarkan prinsip-prinsip pengelolaan perusahaan.
			<br/><br/>
			Dalam melaksanakan dan menyelenggarakan tugas pokok , SBU Transjabodetabek mempunyai fungsi:
			<br/>a. Melaksanakan pengoperasian bus di wilayah Jabodetabek dan Bandara;
			<br/>b. Menyusun dan mengelola kekayaan (aset) di lingkungan SBU Transjabodetabek dengan prinsip transparan dan akuntabel;
			<br/>c. Perumusan, penetapan, dan pelaksanaan kebijakan di dalam lingkungan SBU Transjabodetabek;
			<br/>d. Melakukan pengelolaan keuangan;
			<br/>e. Melakukan pengelolaan kepegawaian dalam hal ini untuk promosi, pengangangkatan dan mutasi pegawai hanya sebagai masukan dan usulan yang diajukan kepada direksi sesuai peraturan perusahaan yang berlaku;
			<br/>f. Melakukan ketatausahaan dan kerumahtanggaan;
			<br/>g. Melakukan koordinasi dengan instansi terkait lainnya untuk kelancaran operasional SBU Transjabodetabek.

