			SBU Pemeliharaan dan Perbaikan Bus merupakan Strategy Bisnis Unit yang bekerja di bidang jasa pemeliharaan dan perbaikan bus dengan pelayanan handal dan terpercaya serta memiliki Sarana dan Prasarana yang lengkap dengan tenaga SDM berkompeten sehingga menghasilkan pemeliharaan dan perbaikan sesuai standar laiak teknis dan laiak jalan.
Â 			<br/><br/>
			Sesuai dengan Keputusan Direksi Perum Pengangkutan Penumpang Djakarta ( PPD ) NOMOR : 15/SEKR/VI/2016, Perum PPD mendirikan SBU Pemeliharaan dan Perbaikan Bus yang bergerak dalam pemeliharaan dan perbaikan bus Perum PPD yang di bentuk pada bulan April tahun 2016. Dengan jaringan yang luas, profesionalisme kerja serta komitmen atas setiap pelayanan, kami percaya mampu menyediakan jasa pemeliharaan dan perbaikan bus yang berkualitas, konsisten dan memiliki nilai tambah demi tercapainya tujuan bersama. Didukung dengan tenaga kerja yang memiliki kompetensi yang tinggi, SBU Pemeliharaan dan Perbaikan Bus memberikan pelayanan yang optimum sehingga bus bisa beroperasi secara maksimal, dan memberikan kepuasan kepada pengguna jasa bus.
			<br/><br/>
			Dengan memaksimalkan keselamatan dan prosedur kendaraan berbasis angkutan massal, unit pemeliharaan dan perbaikan bus sangatlah penting untuk tetap menjaga agar kendaraan selalu aman dan terawat. SBU Pemeliharaan dan Perbaikan Bus ini pun memberikan jasa pemeliharaan dan perbaikan bus pada seluruh armada bus yang di miliki oleh Perum PPD, serta tidak menutup kemungkinan kita menyediakan jasa pemeliharaan dan perbaikan bus pada pihak swasta maupun perusahaan plat merah lainnya.
