				<h2>Dibulan Penuh Keberkahan, Perum PPD Peduli Sekitar Berbagi Kebahagiaan</h2>
				<p>Kamis, 28 April 2022</p>
				<img src="<?php echo base_url();?>/assets/images/news/baksos2.jpeg" class="img-fluid" width="800px" height="800px"><br/><br/>
				Perusahaan Umum Pengangkutan Penumpang Djakarta (Perum
PPD) menyalurkan bantuan kepada masyarakat korban bencana kebakaran di Pasar
Gembrong Jakarta Timur serta memberikan santunan kepada anak-anak Yatim Piatu dari
lingkungan sekitar 6 wilayah kerja Perum PPD di Jabodetabek.<br><br> 

				Hari Raya Idul Fitri merupakan hari dimana setiap orang bersukacita merayakan
kemenangan pada hari yang suci, meskipun Ramadhan tahun ini masih dalam masa
pandemi Covid-19 tidak mengurangi kebahagiaan ditengah segala keterbatasan baik akibat
pandemi maupun musibah yang baru-baru ini terjadi.
</br></br>
				
				Namun, nasib malang menimpa masyarakat di wilayah Pasar Gembrong, Jakarta Timur.
Pasalnya sebanyak 400 bangunan dengan 450 Kepala Keluarga (KK) atau 1.000 Jiwa di 5
RT terpaksa harus mengungsi karena kehilangan tempat tinggal yang telah dihalap si jago
merah. Tidak ada korban jiwa yang menimpa, namun banyak balita yang terpaksa makan,
minum dan tidur seadanya di tenda pengungsian. Olehnya, Perum PPD fokus memberi
bantuan untuk serta kebersihan diri orang dewasa dengan memberikan 200 paket susu
formula dan bubur bayi dan 200 paket alat kebersihan diri yang terdiri dari sabun, sampo,
dan sikat gigi.<br><br>
				
				Pande Putu Yasa selaku Direktur Utama Perum PPD berharap bantuan Perum PPD ini
dapat berguna dan bermanfaat, “Dibulan Ramadhan kali ini Perum PPD menyalurkan 200
paket bantuan kepada korban terdampak bencana kebakaran di Pasar Gembrong Jakarta
Timur. Banyak diantaranya ibu dan bayi yang harus tidur di tenda pengungsian, saya
berharap bantuan dari Perum PPD sedikit meringankan beban mereka.”
<br><br>
				
				Sepanjang bulan ramadhan ini, Perum PPD telah membagikan sebanyak 1.300 paket takjil
bagi masyarakat yang berbuka di tengah perjalanan serta penumpang bus Jabodetabek
Resident Connexion Perum PPD. Selain berbagi takjil dan paket bantuan, Perum PPD juga
memberikan santunan dan buka puasa bersama dengan 720 anak yatim piatu dibawah
binaan berbagai Panti Asuhan yang tersebar diseluruh titik area kerja Perum PPD di
Jabodetabek.<br></br>

<b>Tentang Perum PPD</b></br>
				
				Perusahaan Umum Pengangkutan Penumpang Djakarta (Perum PPD) merupakan
perusahaan milik pemerintah di bidang transportasi umum darat yang menjadi embrio dan
pioneer perkembangan angkutan bus di Jakarta. Bertempat di Jalan D.I. Panjaitan Nomor 1,
Cawang, Jakarta Timur, Perum PPD memiliki beberapa SBU (Sub Bisnis Unit) terdiri dari
SBU Transbusway, SBU Pemeliharaan dan Perbaikan bus, SBU Transjabodetabek, SBU Aset dan Logistik, SBU Trans PPD MAC. Sebagai raja jalanan Perum PPD terus
mengembangkan sayap bisnis layanannya, hingga saat ini Perum PPD melayani
masyarakat dengan pelayanan Jabodetabek Residence Connexion, Jabodetabek Airport
Connexion, Busway, Amari, Pariwisata, dan Karyawan.
